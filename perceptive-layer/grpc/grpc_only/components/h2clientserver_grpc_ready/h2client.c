static inline bool pick_embedded_range(const uint8_t* s1, const uint8_t* e1,
                                       const uint8_t* s2, const uint8_t* e2,
                                       const uint8_t** out_s, int* out_len)
{
    if (s1 && e1 && e1 > s1) { *out_s = s1; *out_len = (int)(e1 - s1); return true; }
    if (s2 && e2 && e2 > s2) { *out_s = s2; *out_len = (int)(e2 - s2); return true; }
    *out_s = NULL; *out_len = 0; return false;
}

static bool h2client_load_certs_into_cfg(void);

