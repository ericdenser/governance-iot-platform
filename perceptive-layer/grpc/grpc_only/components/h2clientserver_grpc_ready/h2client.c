/*
 * Weak declarations for embedded TLS material.
 * When building without embedded certs (h2c / plain TCP mode), these symbols
 * are absent at link time. The __attribute__((weak)) prevents linker errors;
 * h2client_load_certs_into_cfg() checks for NULL before using them.
 */
